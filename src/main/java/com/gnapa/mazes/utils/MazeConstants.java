package com.gnapa.mazes.utils;

/**
 * <p>
 * String constants used to format the maze when printing to
 * stdout.
 * </p>
 *
 * @author Guillermo Narvaez
 */
public class MazeConstants {

    /** String representing an open space in the maze */
    public static final String SPACE = " "; //$NON-NLS-1$

    /** String representing a wall in the maze */
    public static final String WALL = "■"; //$NON-NLS-1$

    /** String representing an entrance to the maze */
    public static final String ENTRANCE = "?"; //$NON-NLS-1$

    /** String representing an exit to the maze */
    public static final String EXIT = "!"; //$NON-NLS-1$

    /** String representing a visited space in the maze */
    public static final String PATH = "•"; //$NON-NLS-1$

    /** String representing the robot in the maze */
    public static final String ROBOT = "@"; //$NON-NLS-1$

}
